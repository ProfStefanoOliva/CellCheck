_current_language = DEFAULT_LANGUAGE


def available_languages() -> list[tuple[str, str]]:
    """Return the supported GUI languages in display order."""
    return list(LANGUAGE_LABELS.items())


def get_language_label(code: str) -> str:
    """Return the display label for a language code."""
    normalized = normalize_language_code(code)
    return LANGUAGE_LABELS.get(normalized, LANGUAGE_LABELS[DEFAULT_LANGUAGE])


def normalize_language_code(code: str | None) -> str:
    """Clamp arbitrary language settings to one supported code."""
    if not code:
        return DEFAULT_LANGUAGE
    lowered = code.replace("-", "_").strip().lower()
    if lowered in LANGUAGE_LABELS:
        return lowered
    root = lowered.split("_", 1)[0]
    return root if root in LANGUAGE_LABELS else DEFAULT_LANGUAGE


def current_language() -> str:
    """Return the in-memory language code currently used by the GUI."""
    return _current_language


def set_current_language(code: str, *, persist: bool = True) -> str:
    """Set the active GUI language and optionally persist it."""
    global _current_language
    normalized = normalize_language_code(code)
    _current_language = normalized
    if persist:
        save_language_setting(normalized)
    return normalized


def load_saved_language(settings: QSettings | None = None) -> str:
    """Load the stored GUI language, defaulting to Italian."""
    active_settings = settings or _build_settings()
    stored = active_settings.value(LANGUAGE_SETTINGS_KEY, DEFAULT_LANGUAGE, type=str)
    return set_current_language(stored, persist=False)


def save_language_setting(code: str, settings: QSettings | None = None) -> str:
    """Persist the chosen GUI language through QSettings."""
    normalized = normalize_language_code(code)
    active_settings = settings or _build_settings()
    active_settings.setValue(LANGUAGE_SETTINGS_KEY, normalized)
    return normalized


def tr(key: str, /, **kwargs: object) -> str:
    """Translate a GUI string, falling back to Italian when needed."""
    template = _lookup_translation(current_language(), key)
    if kwargs:
        try:
            return template.format(**kwargs)
        except Exception:
            return template
    return template


def _lookup_translation(language_code: str, key: str) -> str:
    """Resolve one translation key with Italian fallback."""
    normalized = normalize_language_code(language_code)
    if key in TRANSLATIONS.get(normalized, {}):
        return TRANSLATIONS[normalized][key]
    if key in TRANSLATIONS[DEFAULT_LANGUAGE]:
        return TRANSLATIONS[DEFAULT_LANGUAGE][key]
    return key


def _build_settings() -> QSettings:
    """Create the application settings store for GUI preferences."""
    return QSettings(SETTINGS_ORG, SETTINGS_APP)


def merge_translation(language_code: str, values: Mapping[str, str]) -> None:
    """Extend translations in tests without affecting the fallback logic."""
    normalized = normalize_language_code(language_code)
    TRANSLATIONS.setdefault(normalized, {}).update(values)
